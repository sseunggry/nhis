// 자동 로그아옷 Timer
function logoutTimer(setTime) {
    if((setTime-1) >= 0) {
        $("#logoutTimer .conts-tit em").text(setTime+"초");

        var callTimer = setTimeout(() => {
            logoutTimer(setTime - 1);
        }, 1000);
    } else {
        $("#logoutTimer .conts-tit em").text(setTime+"초");

        // 로그아웃
        setTimeout(() => {
            $("#logoutTimer .btn-close").trigger("click");
        }, 1000);
    }

    $(".close-modal").on('click',function() {
        clearTimeout(callTimer);
    });
}

